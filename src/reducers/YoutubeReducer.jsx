const youtubeReducer = (state, action) => {
  switch (action.type) {
    case "FILTER VIDES":
      return {
        ...state,
      };
    case "":
      return {
        ...state,
      };
    case "":
      return {
        ...state,
      };
    default:
      return state;
  }
};

